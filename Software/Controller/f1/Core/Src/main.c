/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
    static uint32_t delay = 0;
    static uint8_t dir = DCMOTOR_DIR_CLOSE;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */


  LL_APB2_GRP1_EnableClock(LL_APB2_GRP1_PERIPH_AFIO);
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);

  NVIC_SetPriorityGrouping(NVIC_PRIORITYGROUP_4);

  /* System interrupt init*/

  /** NOJTAG: JTAG-DP Disabled and SW-DP Enabled
  */
  LL_GPIO_AF_Remap_SWJ_NOJTAG();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
    SysTick_Config(SystemCoreClock/1100);
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_RTC_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */

    BoardInit();

    MCP23017_Init();

    DCMOT_Init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
    while (1) {
        if(delay < timestamp) {

            delay = timestamp + 100;

            /* button A - duju sklende*/
            if( LL_GPIO_IsInputPinSet(GPIOB, LL_GPIO_PIN_9) ) {

                LED_OFF();

                dir = !dir;

                while( LL_GPIO_IsInputPinSet(GPIOB, LL_GPIO_PIN_9) );

                if(dir) {
                    DCMOT_ValveOpen(DCMOTOR_GAS);
                    //DCMOT_ValveOpen(DCMOTOR_HOTWATER);
                } else {
                    DCMOT_ValveClose(DCMOTOR_GAS);
                    //DCMOT_ValveClose(DCMOTOR_HOTWATER);
                }

            }


            /* button B - virtuves darbines zonos apsvietimas pilnas/dalinis */
            if( LL_GPIO_IsInputPinSet(GPIOB, LL_GPIO_PIN_8) ) {

                MCP23017_TogglePin(&REL10);

                while( LL_GPIO_IsInputPinSet(GPIOB, LL_GPIO_PIN_8) );
            }


            /* button C -  */
            if( LL_GPIO_IsInputPinSet(GPIOA, LL_GPIO_PIN_12) ) {

                MCP23017_TogglePin(&REL11);

                while( LL_GPIO_IsInputPinSet(GPIOA, LL_GPIO_PIN_12) );
            }


            /* button D -  */
            if( LL_GPIO_IsInputPinSet(GPIOA, LL_GPIO_PIN_11) ) {

                MCP23017_TogglePin(&REL12);

                while( LL_GPIO_IsInputPinSet(GPIOA, LL_GPIO_PIN_11) );
            }



            DCMOT_Handler();

        }


    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    }
  /* USER CODE END 3 */
}

/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{
  LL_FLASH_SetLatency(LL_FLASH_LATENCY_1);

  if(LL_FLASH_GetLatency() != LL_FLASH_LATENCY_1)
  {
    Error_Handler();
  }
  LL_RCC_HSI_SetCalibTrimming(16);
  LL_RCC_HSI_Enable();

   /* Wait till HSI is ready */
  while(LL_RCC_HSI_IsReady() != 1)
  {

  }
  LL_RCC_LSI_Enable();

   /* Wait till LSI is ready */
  while(LL_RCC_LSI_IsReady() != 1)
  {

  }
  LL_PWR_EnableBkUpAccess();
  LL_RCC_ForceBackupDomainReset();
  LL_RCC_ReleaseBackupDomainReset();
  LL_RCC_SetRTCClockSource(LL_RCC_RTC_CLKSOURCE_LSI);
  LL_RCC_EnableRTC();
  LL_RCC_PLL_ConfigDomain_SYS(LL_RCC_PLLSOURCE_HSI_DIV_2, LL_RCC_PLL_MUL_8);
  LL_RCC_PLL_Enable();

   /* Wait till PLL is ready */
  while(LL_RCC_PLL_IsReady() != 1)
  {

  }
  LL_RCC_SetAHBPrescaler(LL_RCC_SYSCLK_DIV_1);
  LL_RCC_SetAPB1Prescaler(LL_RCC_APB1_DIV_1);
  LL_RCC_SetAPB2Prescaler(LL_RCC_APB2_DIV_1);
  LL_RCC_SetSysClkSource(LL_RCC_SYS_CLKSOURCE_PLL);

   /* Wait till System clock is ready */
  while(LL_RCC_GetSysClkSource() != LL_RCC_SYS_CLKSOURCE_STATUS_PLL)
  {

  }
  LL_Init1msTick(32000000);
  LL_SYSTICK_SetClkSource(LL_SYSTICK_CLKSOURCE_HCLK);
  LL_SetSystemCoreClock(32000000);
  LL_RCC_SetADCClockSource(LL_RCC_ADC_CLKSRC_PCLK2_DIV_8);
}

